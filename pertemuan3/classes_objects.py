# Buat kelas bernama MyClass, dengan properti bernama x:
class MyClass:
  x = 5

# Buat objek bernama p1, dan cetak nilai x:
p1 = MyClass()
print(p1.x)

# Hapus objek p1:
del p1

# Buat tiga objek dari kelas MyClass:
p1 = MyClass()
p2 = MyClass()
p3 = MyClass()

print(p1.x)
print(p2.x)
print(p3.x)

